
// Aula 27 - Função Math

let num = 9.54578;
let sequencia = (1,2,3,4,5);

console.log(Math.floor(num)); // arredonda para baixo

console.log(Math.ceil(num)); // arredonda para cima

console.log(Math.round(num)); // arredonda para o mais próximo

console.log(Math.random(num)); // retorna um numero randomico

console.log(Math.max(sequencia)); // retorna o maior numero de uma sequencia de valores

console.log(Math.min(sequencia)); // retorna o menor numero de uma sequencia de valores

const aleatorio = Math.round(Math.random() * (10 - 5) + 5);
console.log(aleatorio); // retorna um numero inteiro entre 5 e 10 randomico



